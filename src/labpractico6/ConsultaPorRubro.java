    private void inicializarTabla() {
        modeloTabla = new DefaultTableModel() {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };
        
        modeloTabla.addColumn("Codigo");
        modeloTabla.addColumn("Nombre");
        modeloTabla.addColumn("Precio");
        modeloTabla.addColumn("Categor√≠a");
        modeloTabla.addColumn("Stock");
        
        tablaProductos.setModel(modeloTabla);
    }
    
    private void filtrarPorRubro() {
        String rubroSeleccionado = (String) comboRubros.getSelectedItem();
        
        modeloTabla.setRowCount(0);

        for(Map.Entry <Integer, Producto> aux : DeTodoSa.mercaderia.entrySet()){
           
           if(rubroSeleccionado.equals("Todos") || aux.getValue().getCategoria().equalsIgnoreCase(rubroSeleccionado)){
               String[] fila={
                   aux.getKey().toString(),
                   aux.getValue().getNombre(),
                   String.valueOf(aux.getValue().getPrecio()),
                   aux.getValue().getCategoria(),
                   String.valueOf(aux.getValue().getStock())
               };
               modeloTabla.addRow(fila);
           }
       }
    }
    
    

